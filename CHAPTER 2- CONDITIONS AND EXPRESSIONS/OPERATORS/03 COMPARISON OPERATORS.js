// COMPARISON OPERATORs:-

// these are > , < , >= , <= , == , != ,  === , !==

let a=9;
let b="9";
console.log(a>b);
console.log(a<b);
console.log(a==b);
console.log(a!=b);
console.log(a===b); // this will print true if both the operands as well as there types are same
console.log(a!==b); // this will print true if both the operands as well as there types are not same

