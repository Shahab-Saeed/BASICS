// LOGICAL OPERATORS:-
// these are &&, || , ! operators

// && logical operator:-
let a=9;
let b=6;
console.log(a>b && a==9); // this will return true if every condition is true
console.log(a<b || a==9); // this will return true if any one  condition is true
console.log(!true); // this will give the opposite answer, if true then false and if false then true