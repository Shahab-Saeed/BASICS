// 01-ARITHMETIC OPERATORS:-
// Basics Arithmetic Operators are:-
// +, -, / , *, ** , ++, --, %

let a=9;
let b=6;

// ADDITION OPERATOR:-
console.log("a+b = ", a+b); // + operator will add the numbers/operands.

// SUBTrACTION OPERATOR:-
console.log("a-b = ", a-b); // - operator will subtract the numbers/operands.

// MULTIPLICATION OPERATOR:-
console.log("a*b = ", a*b); // * operator will multiply the numbers/operands.

// DIVISION OPERATOR:-
console.log("a/b = ", a/b); // * operator will divide the numbers/operands.

// MODULUS OPERATOR:-
console.log("a%b =", a%b); // this will divide a by b and gives the remnder

// EXPONENTIAL OPERATOR:-
console.log("a**b =", a**b); // this will gives the a with the power of b answer
// For example:-
// if we wants to take the square of any number we will use:
let c=7;
console.log("the square of seven is: ",7**2);

// if we wants to take the cube of any number we will use:
let d=3;
console.log("the cube of d is: "+d**3);

// INCREMENT OPERATOR:-
// there are two types of increment operators
// a++ --> this will print the value first and after printing increments the value
// ++a --> this will increment first and prints the value

// Example:-
console.log(a++);
console.log(++a);

// DECREMENT OPERATOR:-
// there are two types of decrement operators
// a-- --> this will print the value first and after printing decrements the value
// --a --> this will decrement first and prints the value

console.log(a--);
console.log(--a);

console.log(a)